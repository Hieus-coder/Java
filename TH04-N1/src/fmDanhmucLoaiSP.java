    private void btthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btthemActionPerformed
        // TODO add your handling code here:
        //Khởi tạo các components trên JFrame 
        String maLoai = txtma.getText().trim();
        String tenLoai = txtten.getText().trim();

        // Thêm dòng vào model nếu mã loại không rỗng
        if (!maLoai.isEmpty()) {
            tableModel.addRow(new Object[]{maLoai, tenLoai});
            txtma.setText(""); // Xóa trường nhập sau khi thêm
            txtten.setText("");
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Mã loại không được để trống!");
        }


    }//GEN-LAST:event_btthemActionPerformed

    private void ShowDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ShowDataMouseClicked
        // TODO add your handling code here:
        try {
            int row = ShowData.getSelectedRow();  // Lấy dòng được chọn

            // Lấy giá trị từ dòng được chọn và hiển thị lên JTextField
            oldMaLoai = ShowData.getValueAt(row, 0).toString();  // Lưu giá trị cũ của mã loại
            oldTenLoai = ShowData.getValueAt(row, 1).toString();  // Lưu giá trị cũ của tên loại

            txtma.setText(oldMaLoai);
            txtten.setText(oldTenLoai);
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_ShowDataMouseClicked

