    private void btsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btsuaActionPerformed
        // TODO add your handling code here:
        int selectedRow = ShowData.getSelectedRow();
        if (selectedRow != -1) {  // Kiểm tra xem người dùng đã chọn dòng nào chưa
            // Lưu giá trị cũ
            oldMaLoai = ShowData.getValueAt(selectedRow, 0).toString();
            oldTenLoai = ShowData.getValueAt(selectedRow, 1).toString();

            // Hiển thị giá trị cũ lên JTextField để chỉnh sửa
            txtma.setText(oldMaLoai);
            txtten.setText(oldTenLoai);

            // Cho phép chỉnh sửa mã loại và tên loại
            txtma.setEditable(true);
            txtten.setEditable(true);
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Vui lòng chọn một dòng để sửa!");
        }
    }//GEN-LAST:event_btsuaActionPerformed

